package Basics;

public class MathClass {

	public static void main(String[] args) {
		
	System.out.println(Math.addExact(100, 200));	
	System.out.println(Math.addExact(34567890, 353444647477888874l));
	System.out.println(Math.subtractExact(350, 150));
	System.out.println(Math.subtractExact(3472342734l, 23723128423l));
	System.out.println(Math.multiplyExact(345, 100));
	System.out.println(Math.multiplyExact(567, 4546));
	System.out.println(Math.min(34.56, 45.009));
	System.out.println(Math.max(77.08, 67.45));
	System.out.println(Math.abs(-67.56));
	System.out.println(Math.random());
	

	}

}
